package model;

/**
 *
 * @author Pham Thang
 */
public class Item {
    private Product product;
    private String type;
    private String size;
    private int quantity;
    private User user;

    public Item(Product product, String type, String size, int quantity, User user) {
        this.product = product;
        this.type = type;
        this.size = size;
        this.quantity = quantity;
        this.user = user;
    }

    public Product getProduct() {
        return product;
    }

    public void setProduct(Product product) {
        this.product = product;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
    public Item() {
    }

  
    

  

  
}
